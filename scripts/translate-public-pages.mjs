function deepMerge(target, source) {
  for (const key of Object.keys(source)) {
    const v = source[key];
    if (v && typeof v === "object" && !Array.isArray(v)) {
      target[key] = target[key] && typeof target[key] === "object" ? target[key] : {};
      deepMerge(target[key], v);
    } else {
      target[key] = v;
    }
  }
  return target;
}

